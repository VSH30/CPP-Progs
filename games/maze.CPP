int game(){
    return(pos == END)?0:1;
}

void move(int x){
    int row = (pos+x)/c, col = (pos+x)%c;
    if(arr[row][col] == ' '){
        arr[pos/c][pos%c] = ' ';
        arr[row][col] = 'O';
        pos += x;
        head = "----------------------- ALL GOOD!!!! -------------------------";
    }else{
        head = "----------------------- WALL AHEAD!!! ------------------------";
    }
}

int main(){
    do{
        disp();
        switch(getch()){
            case 'w':case 'W':   move(-c);  break;
            case 's':case 'S':   move(c);   break;
            case 'd':case 'D':   move(1);    break;
            case 'a':case 'A':   move(-1);   break;
            default:    head = "---------------------- INVALID MOVE!!! -----------------------";    break;
        }
    }while(game());

    if(game()==0)
        head = "------------------------ COMPLETED!!! ------------------------";

    disp();
    
    return 0;
}
